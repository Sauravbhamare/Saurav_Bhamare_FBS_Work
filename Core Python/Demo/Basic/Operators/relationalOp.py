###Relational or Comparison Operators
#1. == (Exact equals to)
print(10 == 10)
print(10 == 5)

#2. != (Not equals to)
print( 10 != 5)
print(10 != 10)


#3. >= (Greater than equals to)
print(10 >= 2)
print(10 >= 20)

#4. <= (less than equals to)
print(10 <= 5)
print(10 <= 10)

#5. < (less than)
print(10 < 5)